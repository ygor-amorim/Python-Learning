# This exercise consists in calculating the arithmetic average
# of 4 grades submitted by the user.


a = int(input("Please, enter the first grade: "))
b = int(input("Please, enter the second grade: "))
c = int(input("Please, enter the third grade: "))
d = int(input("Please, enter the fourth grade: "))

avg = (a + b + c + d) / 4

print("The arithmetic average is", avg)
